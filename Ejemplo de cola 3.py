cola=[]

#Agregar elementos a la cola usando append()
cola.append("Perro")
cola.append("Gato")
cola.append("Conejo")

#Imprir la cola actual
print("Cola actual:",cola)

#Eliminar y obtener el elemento más antiguo de la cola usando pop(0)
elemento=cola.pop(0)
print("Elemento eliminado:",elemento)

#Imprimir la cola actual después de la eliminación
print("Cola después de la eliminación:",cola)